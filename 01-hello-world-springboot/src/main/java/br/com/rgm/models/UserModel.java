package br.com.rgm.models;

public class UserModel {

	private String nome;
	private String idade;

	public UserModel(String nome, String idade) {
		this.nome = nome;
		this.idade = idade;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getIdade() {
		return idade;
	}

	public void setIdade(String idade) {
		this.idade = idade;
	}		
}
